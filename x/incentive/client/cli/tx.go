// GetTxCmd returns the transaction commands for this module
func GetTxCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:                        types.ModuleName,
		Short:                      fmt.Sprintf("%s transactions subcommands", types.ModuleName),
		DisableFlagParsing:         true,
		SuggestionsMinimumDistance: 2,
		RunE:                       client.ValidateCmd,
	}

	cmd.AddCommand(CmdSetWithdrawAddress())
	cmd.AddCommand(CmdWithdrawValidatorCommission())
	cmd.AddCommand(CmdWithdrawDelegatorReward())
	cmd.AddCommand(CmdSubmitUpdateMinCommissionRateProposal())
	cmd.AddCommand(CmdSubmitUpdateMaxVotingPowerProposal())
	cmd.AddCommand(CmdSubmitUpdateMinSelfDelegationProposal())
	// this line is used by starport scaffolding # 1

	return cmd
}

func CmdSubmitUpdateMinCommissionRateProposal() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "update-min-commission-rate-proposal [commission]",
		Args:  cobra.ExactArgs(1),
		Short: "Submit an update comission rate proposal",
		RunE: func(cmd *cobra.Command, args []string) error {
			clientCtx, err := client.GetClientTxContext(cmd)
			if err != nil {
				return err
			}

			title, err := cmd.Flags().GetString(cli.FlagTitle)
			if err != nil {
				return err
			}

			description, err := cmd.Flags().GetString(cli.FlagDescription)
			if err != nil {
				return err
			}

			content := types.NewProposalUpdateMinCommission(
				title,
				description,
				args[0],
			)

			from := clientCtx.GetFromAddress()

			depositStr, err := cmd.Flags().GetString(cli.FlagDeposit)
			if err != nil {
				return err
			}
			deposit, err := sdk.ParseCoinsNormalized(depositStr)
			if err != nil {
				return err
			}

			msg, err := v1beta1.NewMsgSubmitProposal(content, deposit, from)
			if err != nil {
				return err
			}

			return tx.GenerateOrBroadcastTxCLI(clientCtx, cmd.Flags(), msg)
		},
	}

	cmd.Flags().String(cli.FlagTitle, "", "title of proposal")
	cmd.Flags().String(cli.FlagDescription, "", "description of proposal")
	cmd.Flags().String(cli.FlagDeposit, "", "deposit of proposal")
	flags.AddTxFlagsToCmd(cmd)

	return cmd
}

